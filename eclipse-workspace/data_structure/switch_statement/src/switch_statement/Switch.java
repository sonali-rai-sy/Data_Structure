package switch_statement;

public class Switch {
	
	public static void main (String args[]) {
		char grade='C';
		
		switch(grade) {
		case 'A':
System.out.println("Choice is A");
break;
		case 'B':
			System.out.println("Choice is B");
			break;
			default:
				System.out.println("Choice is C");
			
				
		
		}
		
	}

}
